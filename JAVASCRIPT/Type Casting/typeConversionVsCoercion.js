//___________________________________________________________Introduction_____________________________________________________________________
/*
Type Conversion হল এক্সপ্লিসিট (পরিষ্কারভাবে) ডেটা টাইপ পরিবর্তনের প্রক্রিয়া। এর মানে, আপনি যখন কোডে স্পষ্টভাবে কোনো একটি টাইপকে অন্য টাইপে রূপান্তর করেন, 
তখন সেটি Type Conversion নামে পরিচিত। এই পদ্ধতিতে আপনি নিজের প্রয়োজন অনুযায়ী টাইপ পরিবর্তন করতে পারেন, এবং এটি একটি মনোযোগী ও ইচ্ছাকৃত কাজ হিসেবে গৃহীত হয়।
*/


/*
let str = "123";
let num = Number(str); // Explicit Type Conversion
console.log(num);      // Output: 123 (as a number)
*/
/*
এখানে str স্ট্রিং টাইপের ভেরিয়েবল ছিল, এবং আমরা এক্সপ্লিসিটভাবে এটিকে Number() কনস্ট্রাক্টরের সাহায্যে সংখ্যা টাইপে রূপান্তর করেছি।

আরও কিছু Type Conversion পদ্ধতি:

String() - ডেটাকে স্ট্রিংয়ে রূপান্তর করে
Number() - ডেটাকে নাম্বারে রূপান্তর করে
Boolean() - ডেটাকে বুলিয়ান ভ্যালুতে রূপান্তর করে
*/



//_______________________________________________Type Coercion_____________________________________________________________
/*
Type Coercion হল ইনপ্লিসিট (স্বয়ংক্রিয়ভাবে) ডেটা টাইপ পরিবর্তনের প্রক্রিয়া। জাভাস্ক্রিপ্ট স্বয়ংক্রিয়ভাবে প্রয়োজন অনুসারে টাইপ পরিবর্তন করে থাকে। 
সাধারণত, এটি তখন ঘটে যখন আপনি বিভিন্ন টাইপের ভেরিয়েবল একসাথে ব্যবহার করেন। এটি ইচ্ছাকৃত নয়, বরং জাভাস্ক্রিপ্টের নিজস্ব প্রয়োজনীয়তা অনুসারে ঘটে।
*/

/*
let str = "5";
let result = str * 2; // Implicit Type Coercion
console.log(result);   // Output: 10 (as a number)
 */          //এখানে str স্ট্রিং টাইপের, কিন্তু * অপারেটরের কারণে জাভাস্ক্রিপ্ট এটিকে স্বয়ংক্রিয়ভাবে নাম্বার টাইপে কনভার্ট করেছে।







