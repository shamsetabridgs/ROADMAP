//___________________________________________________________________Introduction______________________________________________________________
/*
WeakMap জাভাস্ক্রিপ্টে একটি বিশেষ ধরনের Map, যেখানে কী হিসেবে শুধুমাত্র অবজেক্ট (object) ব্যবহার করা যায় এবং এটি গারবেজ কালেকশনের জন্য আরও কার্যকর। 
WeakMap ডেটা স্ট্রাকচারটি সাধারণ Map এর মতো দেখতে হলেও এর কিছু গুরুত্বপূর্ণ পার্থক্য রয়েছে, যা ডেটার মেমোরি ম্যানেজমেন্টকে আরও উন্নত করে।
*/




//_______________________________________________________________Features of Weak Map_______________________________________________________
/*
1. Key হিসেবে শুধুমাত্র Object গ্রহণ করে (প্রিমিটিভ ডেটা টাইপ ব্যবহার করা যায় না)।
2. Weakly referenced (কোনো কীতে আর রেফারেন্স না থাকলে, সেটি গারবেজ কালেকশন দ্বারা মুছে ফেলা যায়)।
3. Iterate করা যায় না; অর্থাৎ WeakMap এর উপর for...of বা forEach ব্যবহার করা যায় না।
4. Size প্রপার্টি নেই, কারণ WeakMap এর এন্ট্রিগুলো গারবেজ কালেকশনের কারণে পরিবর্তিত হতে পারে।
*/




//__________________________________________________________Create and Use Case of WeakMap()________________________________________________
/*

//______________________________1. weakmap() creation:
let weakMap = new WeakMap();

//______________________________2. set(key, value)
let user = { name: "Rahim" };
weakMap.set(user, "Active User");

//______________________________3. get(key)
console.log(weakMap.get(user));                     // Active User

//______________________________4. has(key)
console.log(weakMap.has(user));                     // true

//______________________________5. delete(key)
weakMap.delete(user);
console.log(weakMap.has(user));                     // false

*/






//_______________________________________________________________Apply_____________________________________________________________________
/*
ধরা যাক, আপনি এমন ডেটা সংরক্ষণ করতে চান যা শুধুমাত্র নির্দিষ্ট অবজেক্টের সাথে সম্পর্কিত এবং অবজেক্টটি আর ব্যবহার করা না হলে সেই ডেটা মুছে ফেলা হবে। 
এটি সাধারণত মেমোরি ব্যবস্থাপনার ক্ষেত্রে সহায়ক।
*/


